
class Dog

  def initialize(dog_name)
    @this_dogs_name = dog_name
  end

  def name
    @this_dogs_name
  end

  def name= new_dog_name
    @this_dogs_name = new_dog_name
  end



  def initialize(dog_breed)
    @this_dogs_breed = dog_breed
  end

  def breed
    @this_dogs_breed
  end

  def breed= new_dog_breed
    @this_dogs_breed = new_dog_breed
  end

end
